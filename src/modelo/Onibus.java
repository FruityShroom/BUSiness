package modelo;

public class Onibus 
{
    
    //Atributos
    private String placa;
    private int rota;
        
    
    //Constructor
    public Onibus(String placa) 
    {
        this.placa = placa;
    }

    
    //Getters
    public String getPlaca() 
    {
        return placa;
    }
    
        
    //Setters
    public void setRota(int rota) 
    {
        this.rota = rota;
    }

    
    
    
}
